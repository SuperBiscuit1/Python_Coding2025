
# Ask the user for two numbers
firstnumber = float(input("Please enter one number: "))  # Convert input to float
secondnumber = float(input("Please enter another number: "))  # Convert input to float

# Perform computations and print results
print("Integer Division:", firstnumber // secondnumber)  # Integer division
print("Addition:", secondnumber + firstnumber)  # Addition
print("Floating-Point Division:", firstnumber / secondnumber)  # Floating-point division
print("Multiplication:", firstnumber * secondnumber)  # Multiplication
print("Subtraction:", firstnumber - secondnumber)  # Subtraction

